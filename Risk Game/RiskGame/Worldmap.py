def handle_pygame_events():
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            return False  # Indicates the game should end
    return True  # Indicates the game should continue

def refresh_display(board):
    # Confirm this function is being called
    print("Refreshing display...")

    # Blit the map and territories
    screen.blit(map_image, (0, 0))

    for territory_name, info in territories_info.items():
        # Update territory armies based on the current state of the game
        territory = board.territories.get(territory_name)
        if territory:
            info['armies'] = territory.armies
            update_territory_owner(territory_name, territory.owner.name if territory.owner else "None")

        # Place territory surface
        min_x, min_y = min(point[0] for point in info["points"]), min(point[1] for point in info["points"])
        screen.blit(territory_surfaces[territory_name], (min_x, min_y))

        # Render armies text
        center_x = sum(point[0] for point in info["points"]) / len(info["points"])
        center_y = sum(point[1] for point in info["points"]) / len(info["points"])
        render_text(info['armies'], (center_x, center_y))

    # Update the screen
    pygame.display.flip()

    # Confirm the screen has been updated
    print("Display updated.")

